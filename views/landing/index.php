  <script>
   (function($){
   var previtemTimer=activeitemTimer=nextitemTimer=cleanUpTimer=slideCycle=0,
   onTheMove=false,
   autoCycle=true,
   slideCycleInterval=5000,
   leftOffStage="120%",
   leftOnStage="25%",
   leftprevitem="-25%",
   leftnextitem="75%",
   $slide=$(".slide-content"),
   $items=$slide.find(".single-item"),
   $onStageitem=$items.first(),
   $previtem=$items.last(),
   $nextitem=$items.eq(1),
   $itemInfo=$slide.find(".item-information"),
   $itemInfoHeading=$slide.find(".item-class"),
   $itemInfoText=$slide.find(".item-text");
   
   $items.css({left:leftOffStage});
   $onStageitem.addClass("current").css({left:leftOnStage}).addClass("active");
   $previtem.css({left:leftprevitem}).addClass("active");
   $nextitem.css({left:leftnextitem}).addClass("active");
   $itemInfoHeading.html($onStageitem.data("class"));
   $itemInfoText.html($onStageitem.find("p").html());
   
   var navHTML='<div class="slide-carousel-nav cf">';
   for(var i=0;i<$items.length;i++){
    navHTML+='<button class="slide-carousel-nav-item'+(!i?" current":"")+'" aria-selected="'+!i+'" aria-controls="slide-items-'+i+'" aria-labelledby="slide-items-'+i+'"></button>';
   }
   navHTML+="</div>";
   $slide.append(navHTML);
   var $slideNav=$(".slide-carousel-nav");
   
   function advanceslide(previtem,onStageitem,nextitem){
     if(onTheMove){return;}
     onTheMove=true;
     clearTimeout(slideCycle);
     slideCycle=0;
     var $first=previtem||$previtem,
     $prev=onStageitem||$onStageitem,
     $active=nextitem||$nextitem,
     $next=$nextitem.next().length?$nextitem.next():$items.first();
     
     $onStageitem=$active;
     $nextitem=$next;
     $previtem=$prev;
     $slideNav.children().removeClass("current").attr("aria-selected",false).eq($active.index()).addClass("current").attr("aria-selected",true);
     $itemInfoHeading.css({opacity:0});
     $itemInfoText.css({opacity:0});
     infoTimer=setTimeout(function(){
       $itemInfoHeading.html($onStageitem.data("class")).css({opacity:1});
       $itemInfoText.html($onStageitem.find("p").html()).css({opacity:1});
      },500);
      $first.addClass("active").css({left:"-165%"});
      
      previtemTimer=setTimeout(function(){
        $prev.addClass("active").css({left:leftprevitem});
        $next.removeClass("active").css({left:leftOffStage});
      },100);
      
      activeitemTimer=setTimeout(function(){
        $active.addClass("active").css({left:leftOnStage});
      },300);
      nextitemTimer=setTimeout(function(){
        $next.addClass("active").css({left:leftnextitem});
      },400);
      cleanUpTimer=setTimeout(function(){
        $items.not(".active").css({left:leftOffStage});
        $items.removeClass("active").removeClass("current").attr("aria-selected",false);
        $active.addClass("current").attr("aria-selected",true);
        onTheMove=false;
        if(autoCycle){
          slideCycle=setTimeout(advanceslide,slideCycleInterval);
        }
      },1000);
    }
    slideCycle=setTimeout(advanceslide,slideCycleInterval);
    $(".slide-items").on("click faux_click",function(){
      autoCycle=false;advanceslide();
    });
    $items.on("mouseenter",function(){
      if($(this).hasClass("current")){
        clearTimeout(slideCycle);
        slideCycle=0;
        autoCycle=false;
      }
    }).on("mouseleave",function(){
      if($(this).hasClass("current")&&!slideCycle){
        slideCycle=setTimeout(function(){
          advanceslide();
        },slideCycleInterval);
        autoCycle=true;
      }
    });
    })(jQuery);</script>

